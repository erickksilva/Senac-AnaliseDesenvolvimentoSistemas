    private void btnRespostaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRespostaActionPerformed

        if(cmbBoxMilhar.getSelectedItem().equals("1") && cmbBoxCentena.getSelectedItem().equals("2") && cmbBoxDezena.getSelectedItem().equals("3") && cmbBoxUnidade.getSelectedItem().equals("4")){
            new telasLoreUm().setVisible(true);
            this.dispose(); // Pega a seleção do usuário em cada box. Se a resposta for 1234, retorna positivo.
        }
        else if(cmbBoxMilhar.getSelectedItem().equals("0") && cmbBoxCentena.getSelectedItem().equals("0") && cmbBoxDezena.getSelectedItem().equals("0") && cmbBoxUnidade.getSelectedItem().equals("0")){
            JOptionPane.showMessageDialog(null, "Insira a resposta usando as caixas ao lado, seguindo o padrão MCDU."); // Se estiver zerado, retorna essa mensagem
        }
        else{
            new telasFeedbackGameOver().setVisible(true);
            this.dispose(); // Respostas incorretas resultam em GameOver
        }
    }//GEN-LAST:event_btnRespostaActionPerformed

