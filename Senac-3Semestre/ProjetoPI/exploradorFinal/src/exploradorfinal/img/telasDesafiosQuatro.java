    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed

        try{
            switch (opcoesDesQuatro.getSelection().getActionCommand()) { // Pega a seleção do usuário no radial
                case "insAmbiente":
                textoRadial2.setText("Você olha melhor para os quadros na parede e percebe que"
                                    + "\nas figuras retratadas tem uma aparência pálida e não possuem "
                                    + "\nolhos. As órbitas vazias te encaram como se pudessem ver sua"
                                    + "\nalma."); // Seta texto para o ambiente
                        
                break;
                case "insMonolito":
                textoRadial2.setText("O monolito de pedra possui quatro cubos na parte frontal, "
                                    + "\ncada um com algarismos que vão do 0 ao 9. A figura de uma "
                                    + "\ncaveira com ossos cruzados paira sobre os cubos.");
                //Seta texto para o monolito
                break;
                case "insEsqueletos":
                textoRadial2.setText("Você conta 6 cadeiras e sobre cada uma está sentado um "
                                    + "\nesqueleto. Vermes correm pelos ossos pútridos. "
                                    + "\nCuriosamente, você repara que dois dos esqueletos não" // Seta texto para os esqueletos
                                    + "\npossuem uma das costelas.");
                break;
                default:
                JOptionPane.showMessageDialog(null, "Selecione algo para inspecionar.");
            }
        }catch(Exception e){
            JOptionPane.showMessageDialog(null, "Selecione algo para inspecionar.");
        }
    }//GEN-LAST:event_jButton1ActionPerformed

