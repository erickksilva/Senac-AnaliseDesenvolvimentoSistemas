    private void btnRespostaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRespostaActionPerformed

        if(tFieldResposta.getText().equals("3X - 5 = 16") || tFieldResposta.getText().equals("3x - 5 = 16")){ // Se a resposta for igual a isso, o retorno é positivo
            
            new telasFeedbackPositivo().setVisible(true);
            this.dispose();
        }
        else if(tFieldResposta.getText().equals("3X-5=16") || tFieldResposta.getText().equals("3x-5=16")){ // Aqui pede espaço
            JOptionPane.showMessageDialog(null, "Use espaços para separar os elementos como no exemplo.");
            tFieldResposta.setText("");
        }
        else if(tFieldResposta.getText().equals("")){
            JOptionPane.showMessageDialog(null, "Digite uma resposta no campo ao lado."); // Se tiver em branco, pede para preencher
        }
        else{
            new telasFeedbackGameOver().setVisible(true); // Respostas erradas resultam em GameOver
            this.dispose();
        }
    }//GEN-LAST:event_btnRespostaActionPerformed

