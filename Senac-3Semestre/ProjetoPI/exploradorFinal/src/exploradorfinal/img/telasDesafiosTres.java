    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed

        try{
        switch (buttonGroup1.getSelection().getActionCommand()) { // Pega a seleção do usuário no radial
            case "insMesa":
                textoRadial.setText("A mesa contém somente as moedas e o papiro. Você conta 30"
                                    + "\nmoedas."); // Seta texto para mesa
                break;
            case "insPapiro":
                textoRadial.setText("O papiro mostra a imagem do ídolo e o número 16. Também "
                                    + "\ndiz: 'O lado esquerdo vale três vezes menos cinco "
                                    + "\no direito.'");
                                    //Seta texto para o papiro
                break;
            case "insCamara":
                textoRadial.setText("Você passa os olhos pela câmara e percebe que a balança "
                                    + "\nestá diretamente ligada ao portão. "
                                    + "\nSomente resolvendo o enigma você poderá avançar. "); // Seta texto para a câmara
                break;
            default:
                JOptionPane.showMessageDialog(null, "Selecione algo para inspecionar."); // Caso não selecione nada, esta mensagem retorna
            }
        }catch(Exception e){
            JOptionPane.showMessageDialog(null, "Selecione algo para inspecionar."); // Evita que o console retorne um erro
        }
    }//GEN-LAST:event_jButton1ActionPerformed

